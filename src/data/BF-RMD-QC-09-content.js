// 본 규정은 (주)바이오에프디엔씨(이하 "당사"라 한다.)의 품질관리 업무를 수행하면서 원료, 자재, 제품 등의 표준품에 대한 제반 사항을 규정함으로서 정확하고 효율적인 품질관리 및 제품 제조시의 오류발생을 방지하는데 그 목적이 있다.

// 2. 적용범위
// 본 규정은 당사의 제품 제조 관련 모든 업무담당자에 적용한다.

// 3. 용어의 정의
// 3.1 표준품: 원료, 자재, 제품 등의 품질을 평가하기 위한 기준이 되는 물질로, 품질검사 시 비교 대상으로 사용된다.
// 3.2 관리품: 제품의 품질을 장기적으로 모니터링하기 위해 보관하는 제품 샘플로, 필요시 품질 검사에 사용될 수 있다.

// 4. 책임과 권한
// 4.1 대표이사
// 4.1.1 본 규정에 대한 최종 승인 권한을 가진다.

// 4.2 품질보증책임자
// 4.2.1 표준품 관리 규정의 승인 및 운영을 총괄한다.
// 4.2.2 표준품 관리에 대한 지시 및 감독을 수행한다.

// 4.3 품질보증담당자
// 4.3.1 표준품을 선정하고 이에 대한 보관관리를 수행한다.
// 4.3.2 표준품을 기준으로 원료, 자재, 제품 등의 적부판정을 내린다.
// 4.3.3 표준품의 보관조건 및 보관상태를 정기적으로 확인한다.

// 4. 표준품 선정
// 4.1 제품 표준품
// 4.1.1 신제품은 초도 포장 제품을 표준으로 설정함을 원칙으로 한다.
// 4.1.2 자재의 변경, 원료의 변경 및 외관, 물성변화 등으로 인하여 표준 재설정의 필요가 있을 시 품질보증담당자가 지정하여 설정한 후 품질보증책임자가 승인한다.

// 5. 표준품 선정
// 5.1 제품 표준품
// 5.1.1 신제품은 초도 포장 제품을 표준으로 설정함을 원칙으로 한다.
// 5.1.2 자재의 변경, 원료의 변경 및 외관, 물성변화 등으로 인하여 표준 재설정의 필요가 있을 시 품질보증담당자가 지정하여 설정한 후 품질보증책임자가 승인한다.

// 5.2 자재 표준품
// 5.2.1 자재는 특별한 경우를 제외하고 표준품 관리대상에서 제외한다.
// 5.2.2 특별한 경우, 초도 입고 시 품질보증책임자가 지정한 견본을 표준으로 한다.

// 6. 표준품의 관리 지침
// 6.1 표준품에 "표준품 라벨"을 붙이고, 표준품 손상 또는 변질우려가 없는 적합한 조건의 장소에서 표준품을 보관하고, 품질 검사시험 시 비교, 판정한다.
// 6.2 특별한 보관조건이 라벨상에 지시되어 있는 경우에는 그에 부합되게 보관한다. 일반적으로는 열과 빛이 닿지 않으며 습기를 피할 수 있도록 보관한다.
// 6.3 표준품 제정 시나 변경 시에 "표준품 관리대장"에 기록한 후 관리한다.
// 6.4 원료 표준품의 사용기한이 도달한 경우에는 최근의 동일한 원료로 소분하여 표준품을 설정한다.

// 7. 표준품의 채취량
// 7.1 원료
// 7.1.1 분말(고체)상 원료: 최초 입고시 1 ~ 100g을 채취한다.
// 7.1.2 액상 원료: 최초 입고시 1 ~ 100g을 채취한다.
// 7.1.3 고가의 원료는 소량 채취한다.
// 7.1.4 추출원재료: 필요시에만 적정량을 채취한다.
// 7.2 자재
// 7.2.1 자재는 특별한 경우를 제외하고 표준품 관리대상에서 제외한다.
// 7.3 제품
// 7.3.1 표준품 (초도 포장품 및 교체품): 1개
// 7.3.2 관리품: 1개

// 8. 표준품 및 관리품의 보관 기간 및 교체주기
// 8.1 원료 및 제품의 표준품은 실온에서 제품 사용기한 후 1년간 보관하며, 필요시 연장한다.
// 8.2 관리품의 보관기간은 기본적으로 표준품의 보관 기간과 동일하며, 제품의 생산빈도, 제품특성, 기타사항을 고려하여 품질보증담당자가 정한다.
// 8.3 불필요한 표준품 및 관리품에 대해서는 품질보증 담당자의 판단에 따라 폐기할 수 있다.
// 8.4 표준품의 교체주기
// 8.4.1 표준품의 교체주기는 제품의 특성, 안정성, 사용빈도 등을 고려하여 품질보증책임자가 설정한다.
// 8.4.2 일반적인 교체주기는 다음과 같으나, 제품별로 다르게 설정할 수 있다:
//    a) 안정성이 높은 제품: 2-3년
//    b) 일반 제품(천연물 유래): 1년
//    c) 안정성이 낮은 제품: 6개월-1년
// 8.4.3 교체주기 도래 시, 품질보증담당자는 표준품의 품질을 재평가하여 교체 여부를 결정한다.
// 8.4.4 품질에 이상이 없을 경우, 품질보증책임자의 승인 하에 교체주기를 연장할 수 있다.
// 8.4.5 교체주기 내에도 표준품의 품질에 이상이 발견될 경우, 즉시 새로운 표준품으로 교체한다.

