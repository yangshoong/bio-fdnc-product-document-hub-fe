// 5.2.2 검·교정 주기
// 5.2.2.1 검·교정 주기는 국가 검·교정 주기표를 적용한다.
// 5.2.2.2 위의 5.2.2.1항에 해당되지 않는 장비는 메이커의 지침에 따르거나 자체적으로 결정한 합당한 근거를 바탕으로 결정한다.
// 5.2.3 검·교정 상태의 유지
// 5.2.3.1 품질관리담당자는 검·교정이 완료된 계측기에 대하여 교정기관이 발행한 교정필증을 계측기에 부착하여 식별한다.
// 5.2.3.2 검교정이 완료된 측정장비에 대하여는 측정결과를 무효화 시킬 수 있는 조정 및 방법으로부터 보호 될 수 있도록 한다.
// 5.2.3.3 자체점검이 필요한 계측기를 위해 품질관리담당자는 외부 교정이 된 표준 계측기 또는 표준물질을 입수하여 비교 검정한다. 
// 5.2.3.4 품질관리담당자는 자체적으로 실시한 내부검정성적서를 작성하고, 해당 계측기에 "비교검정 필증"을 부착하여 관리한다.
// 5.2.3.5 계측기 사용자는 온습도 등 환경에 의한 성능저하 요인이 발생하지 않도록 관리하고 규정된 성능을 유지 및 도난, 분실, 파손 등을 방지한다.
// 5.3 부적합 계측기의 처리
// 5.3.1 품질관리담당자는 계측기가 검교정 기준에서 벗어나 있는 것이 발견되면 해당 장비의 사용을 금하고 검사결과에 대한 유효성 확인을 한다.
// 5.3.2 품질관리담당자는 부적합 장비를 검토하여 재검정 또는 재교정 하거나 수리한다. 만일 자체 수리가 어려울 경우 외부에서 수리 하도록 한다.
// 5.3.3 수리된 장비는 초기와 동일한 방법으로 검·교정을 실시하며 검·교정기준에 적합한 경우에만 사용할 수 있다.
// 5.3.4 설비담당은 장비의 수리내용을 ERP 시스템에 기록하고 유지한다.
// 5.3.5 품질관리담당자는 수리가 불가능한 부적합 측정장비는 품질보증책임자의 승인을 얻어 폐기 처분한다.

