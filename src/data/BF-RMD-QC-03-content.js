// 5.1.3 단순한 게이지 및 검사 기구는 기능, 동작상태 및 성능만을 관리한다.
// 5.2 검·교정
// 5.2.1 다음의 방법으로 계측기에 대한 검·교정을 수행한다.
// 5.2.1.1 국가적으로 공인된 검 교정 기관에 의뢰
// 5.2.1.2 공인된 표준기와 자체적으로 비교 검정
